package day05operators;

public class Concatanation01 {

	public static void main(String[] args) {
		
		System.out.println("ali"+"can");
		System.out.println("ali "+ "can");
		System.out.println("ali" + " can");
		System.out.println(3+"4");// en az birisi string ise toplama degil concatenation yapilir
		System.out.println("3"+4+5);//345
        System.out.println("3"+(4+5));	//39	
        System.out.println(3+4+"5"+6);//756
        System.out.println(2*3+"4"+6/2);
        System.out.println("2*3"+4+5);

	}

}
