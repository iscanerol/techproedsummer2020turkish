package day05operators;

public class Increment04 {

	public static void main(String[] args) {
		// int data type inde birtane variable olursturun bu variable yi her seferinde 
	//	bir artirmak icin uc yontemide kullanin
		
		int sayi1 = 10;
		sayi1 = sayi1 + 1;
		System.out.println(sayi1);
		sayi1+=1;
		System.out.println(sayi1);
		sayi1++;
		System.out.println(sayi1);

	}

}
